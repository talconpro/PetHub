import { existsSync, readdirSync, readFileSync, writeFileSync, statSync } from 'node:fs';
import { join, relative } from 'node:path';

const ROOT = process.cwd();
const PETS_DIR = join(ROOT, 'pets');
const OUTPUT_FILE = join(ROOT, 'pets.json');

function readJson(filePath) {
  return JSON.parse(readFileSync(filePath, 'utf8'));
}

function normalizePath(path) {
  return String(path || '').replace(/^\/+/, '');
}

function isDirectory(path) {
  return existsSync(path) && statSync(path).isDirectory();
}

function listPetDirectories() {
  if (!isDirectory(PETS_DIR)) return [];

  return readdirSync(PETS_DIR)
    .map((name) => ({ name, path: join(PETS_DIR, name) }))
    .filter((entry) => isDirectory(entry.path))
    .sort((a, b) => a.name.localeCompare(b.name));
}

function normalizePet(raw, folderName) {
  const id = raw.id || folderName;
  const basePath = `pets/${folderName}/`;
  const previewPath = raw.previewPath || raw.thumbnailPath || raw.iconPath || 'index.png';
  const spritesheetPath = raw.spritesheetPath || 'spritesheet.webp';

  const pet = {
    ...raw,
    id,
    displayName: raw.displayName || raw.name || id,
    description: raw.description || raw.desc || 'A community-contributed Codex Pet.',
    spritesheetPath,
    previewPath,
    basePath,
  };

  if (!pet.category) pet.category = 'Community';
  if (!pet.species) pet.species = 'codex.pet';
  if (!Array.isArray(pet.tags)) pet.tags = [];
  if (!pet.author) pet.author = 'Community';
  if (!pet.version) pet.version = '1.0.0';
  if (!pet.license) pet.license = 'Unspecified';

  return pet;
}

function validatePet(pet, folderName, manifestPath) {
  const errors = [];

  if (!pet.id) errors.push('missing id');
  if (pet.id !== folderName) errors.push(`id "${pet.id}" does not match folder "${folderName}"`);
  if (!/^[a-z0-9]+(?:-[a-z0-9]+)*$/.test(pet.id)) {
    errors.push('id must use lowercase kebab-case');
  }
  if (!pet.displayName) errors.push('missing displayName');
  if (!pet.description) errors.push('missing description');
  if (!pet.spritesheetPath) errors.push('missing spritesheetPath');

  const spritesheetFile = join(PETS_DIR, folderName, pet.spritesheetPath);
  if (!existsSync(spritesheetFile)) {
    errors.push(`missing spritesheet file: ${pet.spritesheetPath}`);
  }

  const previewFile = join(PETS_DIR, folderName, pet.previewPath || 'index.png');
  if (!existsSync(previewFile)) {
    errors.push(`missing preview file: ${pet.previewPath || 'index.png'}`);
  }

  if (errors.length) {
    throw new Error(`${relative(ROOT, manifestPath)}: ${errors.join('; ')}`);
  }
}

function buildCatalog() {
  const pets = [];

  for (const dir of listPetDirectories()) {
    const manifestPath = join(dir.path, 'pet.json');

    if (!existsSync(manifestPath)) {
      console.warn(`Skipping ${relative(ROOT, dir.path)}: missing pet.json`);
      continue;
    }

    const raw = readJson(manifestPath);
    const pet = normalizePet(raw, dir.name);
    validatePet(pet, dir.name, manifestPath);
    pets.push(pet);
  }

  pets.sort((a, b) => a.id.localeCompare(b.id));

  return {
    schemaVersion: 1,
    name: 'PetHub Gallery Catalog',
    description: 'Auto-generated from pets/<pet-id>/pet.json. Do not edit this file manually.',
    repository: 'https://github.com/JiChaoSong/PetHub',
    generatedAt: new Date().toISOString(),
    pets,
  };
}

const catalog = buildCatalog();
writeFileSync(OUTPUT_FILE, `${JSON.stringify(catalog, null, 2)}\n`, 'utf8');
console.log(`Generated pets.json with ${catalog.pets.length} pets.`);
